        $this->idFatorRh = $idFatorRh;
    }

    
    public function getDescricaoFatorRh()
    {
        return $this->descricaoFatorRh;
    }

   
    public function setDescricaoFatorRh($descricaoFatorRh)
    {
        $this->descricaoFatorRh = $descricaoFatorRh;
    }
}
